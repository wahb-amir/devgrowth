export type SkillEntry = {
  canonical: string;
  pattern: RegExp;
  category: SkillCategory;
};

let _compiled: SkillEntry[] | null = null;

// Pre-compiled once at module load — not per page
export function getCompiledSkills(): SkillEntry[] {
  if (_compiled) return _compiled;
  _compiled = [];

  for (const [cat, skills] of Object.entries(SKILLS_DICTIONARY) as [
    SkillCategory,
    string[],
  ][]) {
    for (const skill of skills) {
      const esc = skill.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
      const pattern = EXACT_MATCH_SKILLS.has(skill)
        ? new RegExp(`\\b${esc}\\b`, "i")
        : new RegExp(esc, "i");
      _compiled.push({ canonical: skill, pattern, category: cat });
    }
  }

  return _compiled;
}
